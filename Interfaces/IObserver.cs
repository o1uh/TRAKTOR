namespace Traktor.Interfaces
{
    /// <summary>
    /// Интерфейс для Наблюдателя (Observer/Subscriber).
    /// Определяет метод, который вызывается Субъектом при обновлении.
    /// </summary>
    public interface IObserver
    {
        /// <summary>
        /// Метод, вызываемый Субъектом для уведомления Наблюдателя об изменениях.
        /// </summary>
        /// <param name="subject">Ссылка на Субъекта, который отправил уведомление (опционально, для получения доп. информации).</param>
        /// <param name="message">Сообщение или данные об изменении (опционально).</param>
        void Update(ISubject subject, string message);
        // Для простоты макета передаем строку message. 
        // В более сложных сценариях это мог бы быть объект с данными о событии.
    }
}